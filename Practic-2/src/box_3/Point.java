package box_3;

public class Point {
    private double x = 0.0;
    private double y = 0.0;




    Point(double x, double y){
        this.x = x;
        this.y = y;
    }

    public void setX(double x) {
        this.x = x;
    }

    public void setY(double y) {
        this.y = y;
    }

    Point(){};
}
